     private ArrayList<SubjectsModel> getSubjects(){
         ArrayList arr=new ArrayList();
        try {
            
            con = dbdetail.getCon();
            ps1 = con.prepareStatement("select * from sub");
            ResultSet rs12 = ps1.executeQuery();
            
            while (rs12.next()) {
                //id,year,faculty,sname,ini,scode,lh,th,labh,evh
                SubjectsModel ar=new SubjectsModel();
                ar.setId(rs12.getInt(1));
                ar.setYear(rs12.getString(2));
                ar.setFac(rs12.getString(3));
                ar.setSname(rs12.getString(4));
                ar.setIni(rs12.getString(5));
                ar.setScode(rs12.getString(6));
                ar.setLh(rs12.getInt(7));
                ar.setTh(rs12.getInt(8));
                ar.setLabh(rs12.getInt(9));
                ar.setEvh(rs12.getInt(10));
                arr.add(ar);
                
            }
            
            
            
           
        } catch (SQLException ex) {
            Logger.getLogger(Subject_View.class.getName()).log(Level.SEVERE, null, ex);
        }
         return arr;
    }
