     private SubjectsModel getOneSub(int id){
         SubjectsModel ar=new SubjectsModel();
        try {
            
            //year+fac+sname+ini+scode+lh+th+labh+evh
            con = dbdetail.getCon();
            ps2 = con.prepareStatement("select id,year,faculty,sname,ini,scode,lh,th,labh,evh from sub where id=?");
            ps2.setInt(1, id);
            ResultSet rs11 = ps2.executeQuery();
            while (rs11.next()) {
                ar.setId(rs11.getInt(1));
                ar.setYear(rs11.getString(2));
                ar.setFac(rs11.getString(3));
                ar.setSname(rs11.getString(4));
                ar.setIni(rs11.getString(5));
                ar.setScode(rs11.getString(6));
                ar.setLh(rs11.getInt(7));
                ar.setTh(rs11.getInt(8));
                ar.setLabh(rs11.getInt(9));
                ar.setEvh(rs11.getInt(10));
                
            }
               
            
        } catch (SQLException ex) {
            Logger.getLogger(Subject_View.class.getName()).log(Level.SEVERE, null, ex);
        }
     return ar;
     
     }
