    private void addRoom(int id,String sub,String loca,String tag){
        
        
    try {
        con = dbdetail.getCon();
        b6 = con.prepareStatement("insert into reserv(id,subid,tag,loc) values(?,?,?,?)");
        b6.setInt(1, id);
        b6.setString(2, sub);
        b6.setString(3, tag);
        b6.setString(4, loca);
         b6.execute();
         con.setAutoCommit(false);
         con.close();
         JOptionPane.showMessageDialog(null, "Successfully Added! ");
          
         Reservation rb = new Reservation();
            rb.setVisible(true);
            this.dispose();
             
    } catch (SQLException ex) {
        Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Try Again! ");
    }     
    }
    
    
        private ArrayList<Reserve> getReserve(){
         ArrayList arr=new ArrayList();
        try {
            con = dbdetail.getCon();
            b5 = con.prepareStatement("select subid,tag,loc from reserv");
            ResultSet set = b5.executeQuery();
            
            while (set.next()) {
                //name+emmid+faculty+department+center+building+level+rank123
                Reserve lo=new Reserve();
                lo.setSubid(set.getString(1));
                lo.setTag(set.getString(2));
                lo.setLoc(set.getString(3));
                
                arr.add(lo);       
            } 
        } catch (SQLException ex) {
            Logger.getLogger(Preview.class.getName()).log(Level.SEVERE, null, ex);
        }
        return arr;
    }
