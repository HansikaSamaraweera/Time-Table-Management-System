         public int rowId() {
        
         try {
             int x;
             
             con = dbdetail.getCon();
             
             b3 = con.prepareStatement("select id from reserv where id >= all (select id from reserv)");
             
             ResultSet rs2 = b3.executeQuery();
             
             while (rs2.next()) {
                 
                 x=rs2.getInt(1);
                 x++;
                 
                 return x;
                 
             }
             
             con.close();
             
             return 0; 
         } catch (SQLException ex) {
             Logger.getLogger(RoomBuilding.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        return 0;
        
    }
         
