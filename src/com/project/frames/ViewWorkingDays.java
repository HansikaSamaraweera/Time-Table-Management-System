    private ArrayList<DayDetails> getDetails(){
         ArrayList arr=new ArrayList();
        try {
           
            
            
            con = dbdetail.getCon();
            ps1 = con.prepareStatement("select DId,nodays,monday,tuesday,wednesday,thursday,friday,saturday,sunday,time,timeslot from Details");
            ResultSet rs12 = ps1.executeQuery();
            
            while (rs12.next()) {
                //name+emmid+faculty+department+center+building+level+rank123
                DayDetails ar=new DayDetails();
                ar.setId(rs12.getInt(1));
                ar.setNodays(rs12.getInt(2));
                ar.setMonday(rs12.getString(3));
                ar.setTuesday(rs12.getString(4));
                ar.setWednesday(rs12.getString(5));
                ar.setThursday(rs12.getString(6));
                ar.setFriday(rs12.getString(7));
                ar.setSaturday(rs12.getString(8));
                ar.setSunday(rs12.getString(9));
                ar.setTime(rs12.getString(10));
                ar.setTimeslot(rs12.getString(11));

                
                arr.add(ar);
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(ViewWorkingDays.class.getName()).log(Level.SEVERE, null, ex);
        }
        return arr;
    }
