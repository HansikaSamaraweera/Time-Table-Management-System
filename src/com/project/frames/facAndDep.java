    private void addFac(int id,String name){
        try {
            con = dbdetail.getCon();
            ps3 = con.prepareStatement("insert into fac(id,name) values(?,?) ");
            ps3.setInt(1,id);
            ps3.setString(2,name);
            ps3.execute();
            con.setAutoCommit(false);
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(facAndDep.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    } 
    public int fac_index() {
        
         try {
             int x;
             
             con = dbdetail.getCon();
             
             ps2 = con.prepareStatement("select id from fac where id >= all (select id from fac)");
             
             ResultSet rs2 = ps2.executeQuery();
             
             while (rs2.next()) {
                 
                 x=rs2.getInt(1);
                 x++;
                 
                 return x;
                 
             }
             
             con.close();
             
             
             
             return 0; 
         } catch (SQLException ex) {
             Logger.getLogger(lecturers.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        return 0;
        
    }
