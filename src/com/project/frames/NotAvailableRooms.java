       public ArrayList getRoomId(){
        
        ArrayList a=new ArrayList();
         try {
        
             con = dbdetail.getCon();
             b1 = con.prepareStatement("select id from room");
             
             ResultSet rs11 = b1.executeQuery();
             
             while (rs11.next()) {
                 
                 a.add(rs11.getString(1));
                 
             }
         } catch (SQLException ex) {
             Logger.getLogger(NotAvailableRooms.class.getName()).log(Level.SEVERE, null, ex);
         }
          return a;  
    }
       public ArrayList getTimeSlot(String id){
        
        ArrayList a=new ArrayList();
         try {
        
             con = dbdetail.getCon();
             b2 = con.prepareStatement("select id from room_timetable where roomid=?");
             b2.setString(1,id);
             ResultSet rs11 = b2.executeQuery();
             
             while (rs11.next()) {
                 
                 a.add(rs11.getString(1));
                 
             }
         } catch (SQLException ex) {
             Logger.getLogger(NotAvailableRooms.class.getName()).log(Level.SEVERE, null, ex);
         }
          return a;  
    }
       
       
