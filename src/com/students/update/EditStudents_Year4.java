    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            int i=Integer.parseInt(id.getText());
            String year=(String) ay.getText();
            String sem=(String) s1.getText();
            String programme=(String) pro.getSelectedItem();
            String groupNo=(String) gno.getValue().toString();
            String groupid=gid.getText();
            String subNo=(String)subno.getValue().toString();
            String subgrpNo=gsubid.getText();

            con = (Connection) dbdetail.getCon();
            ps1 = con.prepareStatement("UPDATE studentsY4 SET year=?,semester=?,programme=?, grpno=?,grpid=?,subgno=?,subgid=?  WHERE stuid=? ");
            ps1.setString(1, year);
            if(s1.isSelected())
            {
                sem="S1";
            }
            if(s2.isSelected()){
                sem="S2";
            }
            ps1.setString(2,sem);
            ps1.setString(3, programme);
            ps1.setString(4, groupNo);
            ps1.setString(5,groupid);
            ps1.setString(6, subNo);
            ps1.setString(7,subgrpNo);
            ps1.setInt(8,i);

            ps1.execute();
            JOptionPane.showMessageDialog(null, "Data Update Successful");

            ViewStudents add=new  ViewStudents();
            this.setVisible(false);
            add.setVisible(true);

        } catch (SQLException ex) {
            Logger.getLogger(EditStudents_Year1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

