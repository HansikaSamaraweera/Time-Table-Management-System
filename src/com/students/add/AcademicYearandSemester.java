    private void yearandsemesterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yearandsemesterActionPerformed
        String year=(String) ay.getText();
        String sem=(String) s1.getText();
        String acyearSemester=ays.getText();
        ays.setText(acyearSemester);
        
        if(ay.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Error!!!...Please Fill the Fields...");   
        }
        else if(ay.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Error!!!...Please Fill the Fields...");
        }
        else{
        int x=0;
        try{
            ps1 = con.prepareStatement("select id from yands where id >= all (select id from yands)");

            ResultSet rs1 = ps1.executeQuery();

            while (rs1.next()) {

                x=rs1.getInt(1);
                x++;

            }

            String q = "INSERT INTO yands (id,year,semester,yearAndsemester) values (?,?,?,?)";
            ps = con.prepareStatement(q);
            ps.setInt(1, x);
            ps.setString(2,year);
            if(s1.isSelected())
            {
                sem="S1";
            }
            if(s2.isSelected()){
                sem="S2";
            }
            ps.setString(3,sem);
            ps.setString(4,acyearSemester);

            ps.execute();

            JOptionPane.showMessageDialog(null, "Add");
            AcademicYearandSemester ad=new  AcademicYearandSemester();
            ad.setVisible(true);
            this.setVisible(false);

        }  catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "");
            Logger.getLogger(AcademicYearandSemester.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_yearandsemesterActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        Edit_AcdemicYearandSemester yAnds=new Edit_AcdemicYearandSemester();
        yAnds.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        int selectedForEdit = acedemic.getSelectedRow();
        TableModel model=acedemic.getModel();
        if(selectedForEdit==-1){
            JOptionPane.showMessageDialog(null,"No row selected!!!");
        }
        else{
           yAnds.setVisible(true); 
        }
        
        String idd=model.getValueAt(selectedForEdit,0).toString();
        String y=model.getValueAt(selectedForEdit,1).toString();
        String sem=model.getValueAt(selectedForEdit, 2).toString();
        String s= model.getValueAt(selectedForEdit,3).toString();


        yAnds.id.setText(idd);
        yAnds.ay.setText(y);
        if(sem.equals("S1")){
            yAnds.s1.setSelected(true);
        }
        else{
            yAnds.s2.setSelected(true);
        }
        
        yAnds.ays.setText(s);
    }//GEN-LAST:event_editActionPerformed

